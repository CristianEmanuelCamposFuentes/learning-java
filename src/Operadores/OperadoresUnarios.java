package Operadores;

public class OperadoresUnarios {
    public static void main(String[] args) {
        int i= -5;

        int j = +i;
        System.out.println("j = " + j);

        int k = -i;
        System.out.println("k = " + k);

        int l = -5;
        System.out.println("l = " + l);

        int m = +l;
        System.out.println("m = " + m);
    }
}
