public class PrimitivosBoolean {

    public static void main(String[] args) {
        boolean datoLogico = true;
        boolean datoLogico2 = false;
        System.out.println(datoLogico);
        System.out.println(datoLogico2);

        // Comparaciones
        boolean datoLogico3 = 10 > 5;
        System.out.println(datoLogico3);

        boolean datoLogico4 = 10 == 5;
        System.out.println(datoLogico4);
    }
}
